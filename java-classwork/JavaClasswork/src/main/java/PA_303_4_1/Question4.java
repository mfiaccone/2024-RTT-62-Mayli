package PA_303_4_1;

public class Question4 {
    public static void main(String[] args) {

        int x = 15;

        if (x < 10 || x > 20) {
            System.out.println("Out of range");
        } else if (x >= 10 && x <= 20) {
            System.out.println("In range");
        }

        x = 5;

        if (x < 10 || x > 20) {
            System.out.println("Out of range");
        } else if (x >= 10 && x <= 20) {
            System.out.println("In range");
        }
    }
}
